CR EQU 0DH
LF EQU 0AH
DATA SEGMENT
NUMBERS DW 5000H,8000H,6000H,4000H,7000H
LEN EQU 05H
ASC DB 10 DUP(?)
DB CR, LF, '$'
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
MOV DS,AX
MOV BX,LEN-1
MOV CX,BX
UP1: MOV BX,CX
LEA SI,NUMBERS
UP: MOV AX, [SI]
MOV DX,[SI+2]
CMP AX,DX
JB DOWN
MOV [SI], DX
MOV [SI+2], AX
DOWN: INC SI
INC SI
DEC BX
JNZ UP
DEC CX
JNZ UP1
MOV AH, 4CH
INT 21H
CODE ENDS
END START